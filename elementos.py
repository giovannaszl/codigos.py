elemento = input ('Digite a sigla de um elemento: ')

if elemento == 'c' :
    print ('Carbono \n-6 \n12,01u')
elif elemento == 'h':
    print('Hidrogênio \n-1 \n1,01u')
elif elemento == 'o':
    print ('Oxigênio \n-8 \n16,00u')
elif elemento == 'n':
    print ('Nitrogênio \n-7 \n14,01u')
elif elemento == 'f':
    print ('Fosforo \n-15 \n30,97u')
else:
    print ('A sigla digitada não existe na tabela.')

